b = 0
n = 1
while b < 3 :
    # while b untuk menentukan berapa baris
    k = 0
    while k<3 :
        # while k untuk menentukan berapa kolom
        print(n , end=" ")
        # end fungsinya agar setelah variabel n di print lanjut disebelah bukan dibawah
        n+=1
        # penjumlahan n untuk menentukan jumlah bilangan
        k+=1
    print(" ")
    b+=1